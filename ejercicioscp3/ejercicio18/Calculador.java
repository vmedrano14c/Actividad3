    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        double salario_bruto, salario_neto, retencionn, porcentaje, horas_trab, valor_hora, retencion_valor;
        
        horas_trab = Double.parseDouble(txtHoras_trabajadas.getText());
        valor_hora = Double.parseDouble(txtValor_hora.getText());
        retencionn = Double.parseDouble(txtRetencion.getText());
        porcentaje = retencionn/100;
        
        salario_bruto = horas_trab * valor_hora;
        retencion_valor = salario_bruto * porcentaje;
        salario_neto = salario_bruto - retencion_valor;
        
        txtSalario_bruto.setText(String.valueOf(salario_bruto));
        txtSalario_neto.setText(String.valueOf(salario_neto));
        
        
    }//GEN-LAST:event_btnCalcularActionPerformed

